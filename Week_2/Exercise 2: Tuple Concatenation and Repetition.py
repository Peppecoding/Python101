# Excersise 2 Exercise 2: Tuple Concatenation and Repetition

# Create two tuples: tuple1 with elements (1, 2, 3) and tuple2 with elements (4, 5, 6).
tuple1 = (1, 2, 3)
tuple2 = (4, 5, 6)
# Concatenate tuple1 and tuple2 and store the result in a new tuple called concatenated_tuple.
concatenated_tuple = tuple1 + tuple2
print(concatenated_tuple)

# Repeat tuple1 three times and store the result in a new tuple called repeated_tuple.

repeated_tuple = tuple1 * 3
print(repeated_tuple)